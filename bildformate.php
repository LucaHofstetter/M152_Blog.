										<h5>Beschreibung</h5>
										Das Format für digitale Bilder unterstützt das volle Farbspektrum. Das Scannen und Einlesen von Fotos aus Digitalkameras geschieht häufig im JPEG-Format. JPEG ist, im Gegensatz zu vektorbasierten Formaten, ein pixelbasiertes Format.
										<br>
										<br>
										<h5>Vorteile & Nachteile</h5>
										<ul>
											<li>Vorteil: sehr kompatibel mit vielen Programmen / universell verwendbar</li>
											<li>Nachteil: bei Komprimierung kommt es zu Verlusten von Bildinformationen</li>
