										<h3>PNG</h3>

										<h5>Beschreibung</h5>
										Das PNG-Bildformat bietet die Möglichkeit, kleine Dateien bei bleibend hoher Qualität zu nutzen. So können z.B. Logos und kleine Bilder mit Tausenden von Farbschattierungen korrekt weiterverarbeitet werden. PNG-Bilder können mit transparentem Hintergrund gespeichert werden und werden im Internet meist für kleinere Bilder Icons verwendet.
										<br>
										<br>
										<h5>Vorteile & Nachteile</h5>
										<ul>
											<li>Vorteil: behält eine hohe Qualität mit einer kleinen Dateigrösse</li>
											<li>Nachteil: nicht mit allen Programmen kompatibel</li>
