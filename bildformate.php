										<h3>GIF</h3>

										<h5>Beschreibung</h5>
										Das GIF Format wird für simple Webgrafiken mit beschränkter Farbwahl verwendet. Wegen der Beschränkung auf 256 Farben ist GIF das Bildformat mit der geringsten Speicherplatz-Belegung. GIF-Bilder haben kurze Ladezeiten. Für Fotos und Bilder mit vielen Abstufungen ist dieses Format ungeeignet. GIF ist bestens geeignet für kleine grafische Elemente wie Diagramme, Clip Art oder Buttons. GIF-Bilder können mit transparentem Hintergrund gespeichert werden.
