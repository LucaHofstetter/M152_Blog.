				        <h2>Q&A</h3>
		            <h4>Frage: Auf was zielt das Urheberrecht ab?</h4>
		            <p><b>Antwort: </b>Die Urheber wissenschaftlicher, künstlerischer und literarischer Werken zu schützen.</p>
								<hr>
								<h4>Frage: Worauf werden die Rechte des Urhebers geschützt?</h4>
 							 	<p><b>Antwort: </b>Auf Werke des geistigen Schaffens, wie zum Beispiel Musikstücke, Schriftstücke oder Computerprogramme.</p>
								<hr>
								<h4>Frage: Wann erlischt das Urheberrecht eines Werkes?</h4>
 							 	<p><b>Antwort: </b>Es erlischt 70 Jahre nach dem Tode des Urhebers.</p>
								<hr>
								<h4>Frage: Du hast ein Video erstellt, welches du gerne posten würdest. Ist das in Ordnung?</h4>
 							 	<p><b>Antwort: </b>Das geht, aber man müsste extra bei der Gema nachfragen und einen Lizenzvertrag abschliessen. Besser ist es, Creative Commons-Musik zu verwenden.</p>
								<hr>
								<h4>Frage: Du benutzt auf deiner Homepage Bilder oder Texte, die du von fremden Seiten einfach kopiert hast.
								Darfst du das?</h4>
 							 	<p><b>Antwort: </b>Ja. Aber nur wenn du die Einwilligung des Urhebers hast.</p>
								<hr>
								<h4>Frage: Du hast ein tolles Bild von deiner besten Freundin gemacht und möchtest es gerne posten. Darfst du das?</h4>
 							 	<p><b>Antwort: </b>Ja. Aber ich muss mir vorher eine Genehmigung von ihr einholen.</p>
								<hr>
								<h4>Frage: Du machst ein Video auf einem Konzert und möchtest es anschliessend im Internet veröffentlichen. Geht das?</h4>
 							 	<p><b>Antwort: </b>Nein. Bereits das erstellen von Konzertmitschnitten ist verboten.</p>
								<hr>

								<h4>Frage: Darfst du deine Lieblingsfolge einer TV-Serie in Youtube, Facebook oder auf deine Webpage laden?</h4>
 							 	<p><b>Antwort: </b>Nein, das ist verboten, ausser du hast die ausdrückliche Genehmigung des Urhebers, also von dem, der die Serie produziert hat.</p>
								<hr>

								<h4>Frage: Darf man ein Computerspiel, dass man sich selbst gekauft hat, einmal kopieren, um es einem Freund zu geben?</h4>
								<p><b>Antwort: </b>Nein, Kopien sind nur für den eigenen Gebrauch erlaubt. Sobald du es einem Freund gibst, machst du dich strafbar.</p>
								<hr>
								<h4>Frage: Darf man ein Musikvideo von einer berühmten Band, das man im Internet gefunden hat, auf seine eigene Webpage stellen?</h4>
 							 	<p><b>Antwort: </b>Nein. Die einzige Ausnahme wäre, wenn du die Erlaubnis dieser Band hast.</p>
