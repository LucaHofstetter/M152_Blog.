		            <p>Die Datenschutz-Grundverordnung, oder kurz DSGVO, ist ein europäischer Rechtsakt in Form einer Verordnung. Dieser gilt, anders als Richtlinien, in allen Mitgliedstaaten der EU. Man muss es nicht erst durch nationale Gesetze umgesetzten.<br><br>
								Erklärtes Ziel der DSGVO ist "der Schutz natürlicher Personen bei der Verarbeitung personenbezogener Daten und zugleich die Gewährleistung des freien Verkehrs solcher Daten (Art. 1 Abs. 1 DSGVO)". <br>Beide Ziele waren mit der früher geltenden Datenschutz-Richtlinie (Richtlinie 95/46/EG), die den einzelnen Mitgliedstaaten einigen Umsetzungsspielraum liess, nicht erreicht worden. Daher sah sich der europäische Gesetzgeber veranlasst, den Schutz von personenbezogenen Daten in Europa durch die Datenschutz-Grundverordnung hiermit durchzusetzen.
